# Method 1: Using parentheses
my_tuple = (1, 2, 3, 'a', 'b', 'c')
print("Tuple created using parentheses:", my_tuple)

# Method 2: Using the tuple() constructor
another_tuple = tuple([4, 5, 6, 'x', 'y', 'z'])
print("Tuple created using tuple() constructor:", another_tuple)



# Creating a tuple with different data types
my_tuple = (1, 2.5, 'Hello', True, [3, 4, 5])

# Printing the tuple
print("Tuple with different data types:", my_tuple)
